package in.co.basic;

public class Swichcase {
	public static void main(String[] args) {
		int a = 200;
		int b = 30;
	
		String s= "*";
		
		switch (s) {
		case"+":
			System.out.println(a+b);
			break; 
		case "-":
			System.out.println(a-b);
			break;
		case "*":
			System.out.println(a*b); 
			break;
			 
			default:
				System.out.println();

		
		
		
		
		
		
	}
	}
}
