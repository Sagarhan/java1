package in.co.basic;

public class swichcase2 {
	public static void main(String[] args) {
	int a=30;
	int b=22;
	
	String  v ="-";
	switch (v){
	
	case "+":
	System.out.println(a+b);
	break;
	case "-":
		System.out.println(a-b);
		break;
		
	case"*":
	System.out.println(a*b);
	break;
	
		
	}

		
	}
}

